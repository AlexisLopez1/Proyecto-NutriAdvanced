    private void bGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bGuardarActionPerformed
        
        conexion = new Conexion("alexis", "root", "com.mysql.jdbc.Driver", "NutriAdvanced");
        conexion.conecta();
        
        String id = tPas.getText();
        String imc = FieldIMC.getText();
        String clas = FieldClasifi.getText();
        String pi = FieldPesoIdeal.getText();
        String pg = FieldPorcGrasa.getText();
        String mm = FieldMasaMagra.getText();
        String mb = FieldMetabolismoBasal.getText();
        String rc = FieldRequeriCal.getText();
        String car = FieldCarbohidratos.getText();
        String prot = FieldProteinas.getText();
        String gr = FieldGrasas.getText();
        
        String insert = "insert into Evaluaciones values(null,'"+id+"', current_date(), '"+imc+"', '"+clas+"', '"+pi+"', '"+pg+"', '"+mm+"', '"+mb+"', '"+rc+"', '"+car+"', '"+prot+"', '"+gr+"')";
        
        try
        {
            conexion.insertaRegistro(insert);
            System.out.println("Registrado");
        }
	catch (Exception ex)
	{
		JOptionPane.showMessageDialog(null,"Fallo la Conexi칩n", "Error", JOptionPane.ERROR_MESSAGE);//Error java
	}
        
        JOptionPane.showMessageDialog(null,"La Evaluacion fue exitosa");
        PDF pdf=new PDF();
        pdf.CrearPDF(id, FieldPeso.getText(), FieldTalla.getText(), imc, clas, pi, pg, mm, mb, rc, car, prot, gr);
        
        dispose();
    }//GEN-LAST:event_bGuardarActionPerformed

