void Buscar(String var) 
    {
        try
        {
            conexion = new Conexion("alexis", "root", "com.mysql.jdbc.Driver", "NutriAdvanced");
            conexion.conecta();
            
            String consulta = "select * from Datos_Evaluacion where ID="+var;
            rs=conexion.Consultar(consulta);
        
            if(rs.next());
            {
                tPas.setText((rs.getString("ID")));
                FieldEdad.setText((rs.getString("Edad")));
                FieldPeso.setText((rs.getString("Peso")));
                FieldTalla.setText((rs.getString("Talla")));
                FieldCintura.setText((rs.getString("Cintura")));
                FieldCadera.setText((rs.getString("Cadera")));
            }
            
            String consulta2 = "select Nombre, Apellido from Pacientes where ID ="+var;
            rs=conexion.Consultar(consulta2);
            if(rs.next())
            {
                lNom.setText((rs.getString("Nombre")));
                lAp.setText((rs.getString("Apellido")));
            }
            id=tPas.getText();
        
        }
        catch (SQLException ex)
        {
			
            JOptionPane.showMessageDialog(null,ex, "Error", JOptionPane.ERROR_MESSAGE);
  	  	 
        }
        catch (Exception ex)
        {		
	    JOptionPane.showMessageDialog(null,ex, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }    

